#include <stdio.h>
#include "geometry.h"

void	mtrx44_print(t_mtrx44 m)
{
	printf("| %10.3lf %10.3lf %10.3lf %10.3lf |\n", \
			m[0], m[1], m[2], m[3]);
	printf("| %10.3lf %10.3lf %10.3lf %10.3lf |\n", \
			m[4], m[5], m[6], m[7]);
	printf("| %10.3lf %10.3lf %10.3lf %10.3lf |\n", \
			m[8], m[9], m[10], m[11]);
	printf("| %10.3lf %10.3lf %10.3lf %10.3lf |\n", \
			m[12], m[13], m[14], m[15]);
}
