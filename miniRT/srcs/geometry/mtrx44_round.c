#include "geometry.h"
#include <math.h>

void	mtrx44_round(t_mtrx44 m)
{
	int	i;

	i = -1;
	while (++i < 16)
	{
		if (fabs(m[i]) < g_eps)
			m[i] = 0.;
	}
}
