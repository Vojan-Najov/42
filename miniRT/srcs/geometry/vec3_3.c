#include "minirt_types.h"

void	vec3_sum_vec3(t_vec3 v, t_vec3 u, t_vec3 w)
{
	w[0] = v[0] + u[0];
	w[1] = v[1] + u[1];
	w[2] = v[2] + u[2];
}
